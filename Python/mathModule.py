import math

print(f"The value of pi is : {math.pi}")  # Output: 3.141592653589793 
print(f"The value of e is : {math.e}")  # Output: 2.718281828459045

x = 16
print(f"The square root of {x} is : {math.sqrt(x)}")  # Output: 4.0
print(f"The ceil value of 3.7 is : {math.ceil(3.7)}")  # Output: 4
print(f"The floor value of 3.7 is : {math.floor(3.7)}")  # Output: 3

print(math.prod([1, 2, 3, 4]))  # Output: 24 (1*2*3*4)